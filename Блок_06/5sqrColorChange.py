from pioneer_sdk import *
import time, random

mk=Pioneer()
mk.arm()
time.sleep(1)
mk.takeoff()
try:
    mk.go_to_local_point_body_fixed(0, 0, 1.5, 0)
    time.sleep(5)
    mk.led_control(255, random.randint(0, 100)/100, random.randint(0, 100)/100, random.randint(0, 100)/100)
    mk.go_to_local_point_body_fixed(0, 1, 0, 0)
    time.sleep(5)
    mk.go_to_local_point_body_fixed(0, 0, 0, 3.14/2)
    mk.led_control(255, random.randint(0, 100)/100, random.randint(0, 100)/100, random.randint(0, 100)/100)
    time.sleep(5)
    mk.go_to_local_point_body_fixed(0, 1, 0, 0)
    time.sleep(5)
    mk.go_to_local_point_body_fixed(0, 0, 0, 3.14/2)
    mk.led_control(255, random.randint(0, 100)/100, random.randint(0, 100)/100, random.randint(0, 100)/100)
    time.sleep(5)
    mk.go_to_local_point_body_fixed(0, 1, 0, 0)
    time.sleep(5)
    mk.go_to_local_point_body_fixed(0, 0, 0, 3.14/2)
    mk.led_control(255, random.randint(0, 100)/100, random.randint(0, 100)/100, random.randint(0, 100)/100)
    time.sleep(5)
    mk.go_to_local_point_body_fixed(0, 1, 0, 0)
    time.sleep(5)
    mk.go_to_local_point_body_fixed(0, 0, 0, 3.14/2)
    mk.led_control(255, random.randint(0, 100)/100, random.randint(0, 100)/100, random.randint(0, 100)/100)
    time.sleep(5)
finally:
    mk.land()
    mk.led_control(255, 0, 0, 0)
    time.sleep(5)
    mk.disarm()